/**
 * Converts Float32Array PCM data to Int16Array PCM data.
 * @param input Float32Array with values between -1.0 and 1.0
 * @returns Int16Array with values between -32768 and 32767
 */
export function float32ToInt16(input: Float32Array): Int16Array {
  const output = new Int16Array(input.length);
  for (let i = 0; i < input.length; i++) {
    const s = Math.max(-1, Math.min(1, input[i]));
    output[i] = s < 0 ? s * 0x8000 : s * 0x7FFF;
  }
  return output;
}

/**
 * Converts an Int16Array to a Base64 encoded string.
 * @param input Int16Array PCM data
 * @returns Base64 encoded string
 */
export function int16ToBase64(input: Int16Array): string {
  const buffer = new Uint8Array(input.buffer);
  let binary = '';
  for (let i = 0; i < buffer.byteLength; i++) {
    binary += String.fromCharCode(buffer[i]);
  }
  return btoa(binary);
}
